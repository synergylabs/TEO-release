#include "tot-helper.hpp"

namespace libtot {
    int generate_keypair(std::vector<int8_t> &pubkey, std::vector<int8_t> &privkey) {
        AsymmetricEncryptionKeySet keyset;

        pubkey.clear();
        privkey.clear();
        pubkey.resize(AsymmetricEncryptionKeySet::FULL_PK_SIZE);
        privkey.resize(AsymmetricEncryptionKeySet::FULL_SK_SIZE);

        keyset.get_full_pk(reinterpret_cast<uint8_t *>(&pubkey[0]), pubkey.size());
        keyset.get_full_sk(reinterpret_cast<uint8_t *>(&privkey[0]), privkey.size());

        return 0;
    }
}